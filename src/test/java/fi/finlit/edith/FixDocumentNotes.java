    public static void main(String[] args) throws IOException {
        Pattern pattern = Pattern.compile("(start|end)\\d+");
        for (File file : new File("../edith_docs").listFiles()) {
            if (file.getName().endsWith(".xml")) {
                String str = FileUtils.readFileToString(file, "UTF-8");
                Matcher matcher = pattern.matcher(str);
                while (matcher.find()) {
                    String group = matcher.group();
                    String prefix = group.startsWith("start") ? "start" : "end";
                    String num = group.substring(prefix.length());
                    Long key = Long.parseLong(num);
                    if (replacements.containsKey(key)) {
                        str = str.replace(group, prefix + replacements.get(key));                        
                    }
                }
                FileUtils.writeStringToFile(file, str, "UTF-8");
            }
        }
    }

